public class Solution {
    Integer distanza;
    Long Seed;

    public Solution(Integer distanza, Long seed) {
        this.distanza = distanza;
        Seed = seed;
    }

    public Integer getDistanza() {
        return distanza;
    }

    public void setDistanza(Integer distanza) {
        this.distanza = distanza;
    }

    public Long getSeed() {
        return Seed;
    }

    public void setSeed(Long seed) {
        Seed = seed;
    }






}
